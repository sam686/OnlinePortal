package com.sam.onlineportal.bo;

public class ProductInfo {

	private String name;
	private Double cost;
	
	public void setCost(Double cost) {
		this.cost = cost;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public double getCost() {
		return cost;
	}
	public void setCost(double cost) {
		this.cost = cost;
	}
		
}
